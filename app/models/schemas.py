class Job(BaseModel):
    """Full job model used by the orchestrator and API."""
    model_config = ConfigDict(from_attributes=True)

    id: str
    status: JobStatus = JobStatus.PROCESSING
    stage: str = "initialized"
    script_text: str = ""
    video_style: str = "documentary"
    target_audience: str = "general"
    preferences: SelectionPreferences = Field(default_factory=SelectionPreferences)
    created_at: str = ""
    completed_at: Optional[str] = None
    progress: Optional[JobProgress] = None
    visual_cues: List[VisualCue] = Field(default_factory=list)
    search_results: Optional[List[SearchResult]] = None
    match_results: Optional[Dict[str, MatchResult]] = None
    selections: Optional[Dict[str, RankedSelection]] = None
    summary: Optional[Dict[str, Any]] = None
    total_cost: float = 0.0
    error: Optional[str] = None


