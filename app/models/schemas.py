"""
B-Roll Scout - Pydantic Models
Defines all request/response types and internal data structures.
"""

from pydantic import BaseModel, Field, ConfigDict, field_validator
from typing import Optional, List, Dict, Any
from datetime import datetime
from enum import Enum
import re


# ============================================================================
# Enums
# ============================================================================

class JobStatus(str, Enum):
    PENDING = "pending"
    PROCESSING = "processing"
    COMPLETE = "complete"
    COMPLETED = "completed"
    PARTIAL = "partial"
    FAILED = "failed"


class TranscriptSource(str, Enum):
    CACHED = "cached_transcript"
    YOUTUBE_MANUAL = "youtube_captions"
    YOUTUBE_AUTO = "youtube_auto_captions"
    WHISPER = "whisper_transcription"
    NONE = "no_transcript"


# ============================================================================
# Request Models
# ============================================================================

class JobCreateRequest(BaseModel):
    """Request body for creating a new B-roll search job."""
    script: str = Field(..., min_length=100, description="Tamil script text")
    editor_id: str = Field(default="default_editor", description="Editor identifier")
    
    @field_validator("script")
    @classmethod
    def validate_script(cls, v: str) -> str:
        if len(v.strip()) < 100:
            raise ValueError("Script must be at least 100 characters")
        return v.strip()


class FeedbackRequest(BaseModel):
    """Request body for submitting feedback on a result."""
    rating: int = Field(..., ge=1, le=5, description="Rating from 1-5")
    clip_used: bool = Field(default=False, description="Whether the clip was used")
    notes: Optional[str] = Field(default=None, max_length=1000, description="Optional notes")


class SettingsUpdateRequest(BaseModel):
    """Request body for updating a single setting."""
    setting_key: str
    setting_value: Any


class BulkSettingsUpdateRequest(BaseModel):
    """Request body for bulk updating settings."""
    settings: Dict[str, Any]


class ChannelResolveRequest(BaseModel):
    """Request body for resolving a YouTube channel."""
    channel_url: str


# ============================================================================
# Segment Model
# ============================================================================

class Segment(BaseModel):
    """A script segment generated by the translator."""
    model_config = ConfigDict(from_attributes=True)
    
    segment_id: str = Field(..., pattern=r"^seg_\d{3}$")
    title: str
    summary: str
    visual_need: str
    emotional_tone: str
    key_terms: List[str] = Field(default_factory=list)
    search_queries: List[str] = Field(default_factory=list)
    estimated_duration_seconds: int = Field(default=60, ge=10)
    
    @field_validator("segment_id")
    @classmethod
    def validate_segment_id(cls, v: str) -> str:
        if not re.match(r"^seg_\d{3}$", v):
            raise ValueError("segment_id must match format seg_XXX")
        return v


